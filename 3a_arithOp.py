'''
a)	Explain the following arithmetic operators with examples:
1.	Addition (`+`)
2.	Subtraction (`-`)
3.	Multiplication (``)
4.	Division (`/`)
5.	Floor Division (`//`)
6.	Modulus (`%`)
7.	Exponentiation (``)
'''
'''
1. Addition (+)
The addition operator adds two numbers together.
'''
a = 5
b = 3
result = a + b
print(result)  # Output: 8

'''
2. Subtraction (-)
The subtraction operator subtracts one number from another.
'''
a = 5
b = 3
result = a - b
print(result)  # Output: 2

'''
3. Multiplication (*)
The multiplication operator multiplies two numbers.
'''
a = 5
b = 3
result = a * b
print(result)  # Output: 15

'''
4. Division (/)
The division operator divides one number by another and returns a float.
'''
a = 5
b = 3
result = a / b
print(result)  # Output: 1.6666666666666667

'''
5. Floor Division (//)
The floor division operator divides one number by another and returns the largest integer less than or equal to the result.
'''
a = 5
b = 3
result = a // b
print(result)  # Output: 1

'''
6. Modulus (%)
The modulus operator returns the remainder of the division of one number by another.
'''
a = 5
b = 3
result = a % b
print(result)  # Output: 2

'''
7. Exponentiation (**)
The exponentiation operator raises one number to the power of another.
'''

a = 5
b = 3
result = a ** b
print(result)  # Output: 125
